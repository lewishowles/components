import { fileURLToPath, URL } from "node:url";
import { readFileSync, readdirSync } from "node:fs";
import { join, relative } from "node:path";

/**
 * Recursively find all `.d.ts` files under the given directory, returning
 * their absolute paths.
 *
 * @param  {string}  dir
 *     The directory to scan.
 * @returns  {string[]}
 */
function findDeclarationFiles(dir) {
	const results = [];

	for (const entry of readdirSync(dir, { withFileTypes: true })) {
		if (entry.isDirectory()) {
			results.push(...findDeclarationFiles(join(dir, entry.name)));
		} else if (entry.name.endsWith(".d.ts")) {
			results.push(join(dir, entry.name));
		}
	}

	return results;
}

/**
 * Generate a barrel declaration file that re-exports every co-located `.d.ts`
 * file discovered under the given source directory.
 *
 * @param  {string[]}  absolutePaths
 *     Absolute paths of the co-located declaration files.
 * @param  {string}  groupDir
 *     Absolute path of the group's source directory (`src/composables` etc.).
 * @returns  {string}
 */
function generateBarrel(absolutePaths, groupDir) {
	const exports = absolutePaths.map((absolutePath) => {
		const rel = relative(groupDir, absolutePath).replace(/\.d\.ts$/, ".js");

		return `export * from "./${rel}";`;
	});

	return (
		[
			"// This file is auto-generated by publish-declarations.js. Do not edit directly.",
			...exports,
		].join("\n") + "\n"
	);
}

/**
 * Emit hand-authored co-located `.d.ts` files and auto-generated barrel
 * declarations into `dist/` alongside the compiled entry points.
 *
 * Co-located files are emitted at their path relative to the group source
 * directory. The barrel for each group is generated from the discovered files,
 * so it always stays in sync without manual maintenance.
 */
export function publishDeclarations() {
	const root = fileURLToPath(new URL("../..", import.meta.url));

	const groups = [
		{ dir: join(root, "src/composables"), entryName: "composables" },
		{ dir: join(root, "src/utilities"), entryName: "utilities" },
	];

	return {
		name: "publish-declarations",

		generateBundle() {
			for (const { dir, entryName } of groups) {
				const declarationFiles = findDeclarationFiles(dir);

				// Emit each co-located declaration file at its relative path.
				for (const absolutePath of declarationFiles) {
					this.emitFile({
						type: "asset",
						fileName: relative(dir, absolutePath),
						source: readFileSync(absolutePath, "utf8"),
					});
				}

				// Emit the auto-generated barrel as the named entry point.
				this.emitFile({
					type: "asset",
					fileName: `${entryName}.d.ts`,
					source: generateBarrel(declarationFiles, dir),
				});
			}
		},
	};
}
